int		ft_iterative_factorial(int nb)
{
	int i;
	int j;

	i = 1;
	j = nb;
	if (nb == 0)
		return (1);
	if (nb > 12 || nb < 0)
		return (0);
	while (i < j)
	{
		nb = i * nb;
		i++;
	}
	return (nb);
}
